package co.cask.cdap.api.lineage.operation;

import java.util.ArrayList;
import java.util.Arrays;
import java.util.Collections;
import java.util.List;

/**
 * Represents a read operation from a data source into a collection of outputs.
 */
public class Read extends Operation {
  private final EndPoint source;
  private final List<String> outputs;

  /**
   * Create an instance of read operation.
   * @param name the name of the operation
   * @param description the description associated with the operation
   * @param source the source for the operation
   * @param outputs the array of outputs to be generated
   */
  public Read(String name, String description, EndPoint source, String ... outputs) {
    this(name, description, source, Arrays.asList(outputs));
  }

  /**
   * Create an instance of read operation.
   * @param name the name of the operation
   * @param description the description associated with the operation
   * @param source the source for the operation
   * @param outputs the list of outputs to be generated
   */
  public Read(String name, String description, EndPoint source, List<String> outputs) {
    super(name, Type.READ, description);
    this.source = source;
    this.outputs = Collections.unmodifiableList(new ArrayList<>(outputs));
  }

  /**
   * @return the source associated with the read operation
   */
  public EndPoint getSource() {
    return source;
  }

  /**
   * Get the list of outputs generated by this read operation
   * @return the list of outputs
   */
  public List<String> getOutputs() {
    return outputs;
  }
}
